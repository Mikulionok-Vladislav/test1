<?php

namespace offices;

abstract class Office
{
    protected $name;
    protected $address;

    public function getName() {
        return $this->name;
    }

    public function setName($name) {
        $this->name = $name;
    }

    public function getAddress() {
        return $this->address;
    }

    public function setAddress($address) {
        $this->address = $address;
    }

    abstract public function fullName();
}